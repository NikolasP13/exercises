#Créer une fonction prenant 2 nombres en paramètre et retournant leur addition et soustraction.
def sommes_des_nombres(nombre1, nombre2) :
    sommes_total = nombre1 + nombre2
    return sommes_total
def soustraction_des_nombres(nombre1, nombre2) :
    soustraction_total = nombre1 - nombre2
    return soustraction_total
print(sommes_des_nombres(10,-12))
print(soustraction_des_nombres(10,-12))